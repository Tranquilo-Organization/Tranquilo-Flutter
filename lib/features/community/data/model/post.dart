class Post {
  final String author;
  final String date;
  final String content;
  final int upvotes;
  final int downvotes;
  final int comments;

  Post({
    required this.author,
    required this.date,
    required this.content,
    required this.upvotes,
    required this.downvotes,
    required this.comments,
  });
}
